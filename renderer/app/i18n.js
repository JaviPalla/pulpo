// LANG se resuelve en boot() (config.language || idioma del sistema). "es" por defecto.
let LANG = "es";

/** Traduce una cadena española a la del idioma activo (devuelve la misma en español).
 *  Soporta interpolación con marcadores {nombre}: t("Hola {quien}", { quien: "tío" }). */
function t(es, params) {
  let out = LANG === "en" ? (I18N_EN[es] ?? es) : es;
  if (params) for (const key in params) out = out.split(`{${key}}`).join(params[key]);
  return out;
}

/** Idioma activo ("es" | "en"). */
function currentLang() {
  return LANG;
}

/** Resuelve el idioma a partir de la config: explícito, o el del sistema (es-* → es, resto → en). */
function resolveLang(cfg) {
  // Override por URL (?lang=en) para previsualizar/capturar; tiene prioridad sobre todo.
  const override = new URLSearchParams(location.search).get("lang");
  if (override === "es" || override === "en") return override;
  if (cfg?.language === "es" || cfg?.language === "en") return cfg.language;
  return String(cfg?.systemLocale || "es").toLowerCase().startsWith("es") ? "es" : "en";
}

/** Fija LANG (lo llama boot tras leer config). */
function applyLang(cfg) {
  LANG = resolveLang(cfg);
  document.documentElement.lang = LANG;
}

/** Cambia el idioma: persiste y recarga la ventana para repintar todo en el nuevo idioma. */
async function setLanguage(lang) {
  await window.monstro.setConfig({ language: lang });
  location.reload();
}

/** Reemplaza el primer nodo de texto no vacío de un elemento, preservando hijos (svg, span) y espacios. */
function localizeTextNode(el, key) {
  for (const node of el.childNodes) {
    if (node.nodeType === Node.TEXT_NODE && node.nodeValue.trim()) {
      const lead = node.nodeValue.match(/^\s*/)[0];
      const tail = node.nodeValue.match(/\s*$/)[0];
      node.nodeValue = lead + t(key) + tail;
      return;
    }
  }
  el.appendChild(document.createTextNode(t(key)));
}

/** Traduce el HTML estático marcado con data-i18n / data-i18n-title / data-i18n-placeholder. */
function localizeStatic(root = document) {
  if (LANG === "es") return; // español = sin cambios (las claves ya están en español)
  root.querySelectorAll("[data-i18n]").forEach((el) => localizeTextNode(el, el.getAttribute("data-i18n")));
  root.querySelectorAll("[data-i18n-title]").forEach((el) => (el.title = t(el.getAttribute("data-i18n-title"))));
  root.querySelectorAll("[data-i18n-placeholder]").forEach((el) => (el.placeholder = t(el.getAttribute("data-i18n-placeholder"))));
}
